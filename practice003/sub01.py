# integer addition
def sub01():
    
    numberOne = 1
    numberTwo = 2
    
    sum = numberOne + numberTwo
    
    return sum

# general type addition
def sub02( v01, v02 ):
    
    return v01 + v02

# general type subtraction
def sub03( v01, v02 ):
    
    return v01 - v02


# general type multiply
def sub04( v01, v02 ):
    
    return v01 * v02

# general type division
def sub05( v01, v02 ):
    
    return v01 / v02

#  The integer value of the quotient 
# v01 = 5,  v02 = 2 , --> 5 // 2 = 2
def sub06( v01, v02 ):
    
    return v01 // v02

# remainder
# v01 = 5,  v02 = 2 , --> 5 % 2 = 1
def sub07( v01, v02 ):
    
    return v01 % v02


#  v01 to the power of v02
#  v01 = 2, V02 = 3 -->  2^3 = 2 * 2 * 2 = 8
def sub08( v01, v02 ):
    
    return v01 ** v02



